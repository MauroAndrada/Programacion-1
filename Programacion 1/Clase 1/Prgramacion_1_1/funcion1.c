int suma(int num1,int num2)
{
    int total;
    total=num1+num2;
    return total;
}

int resta(int num1,int num2)
{
    int total;
    total=num1-num2;
    return total;
}

int multiplicacion(int num1,int num2)
{
    int total;
    total=num1*num2;
    return total;
}

int division(int num1,int num2)
{
    float total;
    total=num1/num2;
    return total;
}
